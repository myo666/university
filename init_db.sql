-- ============================================
-- 1. 创建数据库
-- ============================================
CREATE DATABASE LoginDB;
GO

-- ============================================
-- 2. 切换到 LoginDB
-- ============================================
USE LoginDB;
GO

-- ============================================
-- 3. 创建 user 表（user 是 SQL Server 保留关键字，用方括号包裹）
-- ============================================
CREATE TABLE [user] (
    id       INT IDENTITY(1,1) PRIMARY KEY,   -- 自增主键
    username NVARCHAR(50) NOT NULL UNIQUE,    -- 用户名，不可重复
    password NVARCHAR(50) NOT NULL            -- 密码
);
GO

-- ============================================
-- 4. 插入一条测试数据
-- ============================================
INSERT INTO [user] (username, password) VALUES ('admin', '123456');
GO

-- ============================================
-- 5. 验证
-- ============================================
SELECT * FROM [user];
GO
